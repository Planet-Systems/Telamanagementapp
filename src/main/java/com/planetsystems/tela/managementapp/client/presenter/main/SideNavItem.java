package com.planetsystems.tela.managementapp.client.presenter.main;

public class SideNavItem {

	private String id;
	private String icon;
	private String name;
	
	
	public SideNavItem(String id, String icon, String name) {
		super();
		this.id = id;
		this.icon = icon;
		this.name = name;
	}


	public String getId() {
		return id;
	}


	public String getIcon() {
		return icon;
	}


	public String getName() {
		return name;
	}
	
	
	

	
	

}
